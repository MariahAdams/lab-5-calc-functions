/* globals sum, difference, product, quotient */
/* exported add, subtract, multiply, divide */

// add
function add(x, y) {
    var sum = x + y;

    return sum;
}

// subtract
function subtract(x, y) {
    var difference = x - y;

    return difference;
}

// multiply
function multiply(x, y) {
    var product = x * y;

    return product;
}

// divide
function divide(x, y) {
    var quotient = x / y;

    return quotient;
}

